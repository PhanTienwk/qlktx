    void showDichVu() {
        int sophong = new PhongDAO().SoLuongPhong();

        double sonuocmoi = new ThongTinHoaDonDAO().TongSoDichVu("soNuocMoi");
        double sonuoccu = new ThongTinHoaDonDAO().TongSoDichVu("soNuocCu");
        double sodienmoi = new ThongTinHoaDonDAO().TongSoDichVu("soDienMoi");
        double sodiencu = new ThongTinHoaDonDAO().TongSoDichVu("soDienCu");

        float tiennuoc = new DichVuDAO().GiaDichVu("tienNuoc");
        float tiendien = new DichVuDAO().GiaDichVu("tienDien");
        float tienmang = new DichVuDAO().GiaDichVu("tienMang");

        double tiendvnuoc = (double) Math.round((sonuocmoi - sonuoccu) * tiennuoc * 10) / 10;
        double tiendvdien = (double) Math.round((sodienmoi - sodiencu) * tiendien * 10) / 10;
        double tiendvmang = (double) Math.round(sophong * tienmang * 10) / 10;

        Locale localeVN = new Locale("vi", "VN");
        NumberFormat currencyVN = NumberFormat.getCurrencyInstance(localeVN);
        String str1 = currencyVN.format(tiendvnuoc);
        String str2 = currencyVN.format(tiendvdien);
        String str3 = currencyVN.format(tiendvmang);
        String str4 = currencyVN.format(tiendvnuoc + tiendvmang + tiendvdien);

        txttiennuoc.setText(str1);
        txttiendien.setText(str2);
        txttienmang.setText(str3);
        txttiendichvu.setText(str4);
    }

    void setBieuDo() {

        float tiennuoc = new DichVuDAO().GiaDichVu("tienNuoc");
        float tiendien = new DichVuDAO().GiaDichVu("tienDien");
        float tienmang = new DichVuDAO().GiaDichVu("tienMang");
        chart.clear();
        chart.addLegend("Tiền điện", new Color(245, 189, 135));
        chart.addLegend("Tiền nước", new Color(135, 189, 245));
        chart.addLegend("Tiền dịch vụ", new Color(189, 135, 245));
        List<ThongTinPhong> listphong = new PhongDAO().getAllThongTinPhong();
        for (ThongTinPhong phong : listphong) {
            double tiendvnuoc = 0.0;
            double tiendvdien = 0.0;
            double tiendvmang = 0.0;
            double tiendichvu = 0.0;

            double sonuoc = new ThongTinHoaDonDAO().SoNuocTheoPhong(phong.getMaPhong());
            double sodien = new ThongTinHoaDonDAO().SoDienTheoPhong(phong.getMaPhong());

            tiendvnuoc = (double) Math.round(sonuoc * tiennuoc * 10) / 10;
            tiendvdien = (double) Math.round(sodien * tiendien * 10) / 10;
            tiendvmang = (double) Math.round(tienmang * 10) / 10;
            tiendichvu = (double) Math.round((tiendvnuoc + tiendvdien + tiendvmang) * 10) / 10;

            double test[] = {tiendvdien, tiendvnuoc, tiendichvu};
            chart.addData(new ModelChart(phong.getMaPhong(), test));
        }
        chart.start();
    }

