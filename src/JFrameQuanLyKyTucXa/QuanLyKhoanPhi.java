    void showTable() {
        list = new ThongTinHoaDonDAO().getAllThongTinDichVu();
        Model.setRowCount(0);
        for (ThongTinHoaDon hd : list) {
            double tienphi = ((hd.getSoNuocMoi() - hd.getSoNuocCu()) * tiennuoc) + tienmang + ((hd.getSoDienMoi() - hd.getSoDienCu()) * tiendien);
            Model.addRow(new Object[]{
                hd.getMaPhong(), hd.getSoNuocCu(), hd.getSoNuocMoi(), hd.getSoDienCu(), hd.getSoDienMoi(), tienphi
            });
        }
    }

    private void showTienDichVu() {
        Connection con = KetNoiSQL.getConnection();
        String sql = "select * from thongtindichvu where dichVu='dichvu'";

        try {
            PreparedStatement ps = con.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                txttiendien.setText(rs.getString("tienDien"));
                txttiennuoc.setText(rs.getString("tienNuoc"));
                txttienmang.setText(rs.getString("tienMang"));
            }

            tiendien = Float.parseFloat(txttiendien.getText().trim());
            tiennuoc = Float.parseFloat(txttiennuoc.getText().trim());
            tienmang = Float.parseFloat(txttienmang.getText().trim());
            con.close();
        } catch (Exception e) {
        }

    }

