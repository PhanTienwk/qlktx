    private void deleteSVMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteSVMouseClicked
        // TODO add your handling code here:
        if (count == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn sinh viên để xóa!");
        } else {
            int test = JOptionPane.showConfirmDialog(this, "Bạn chắc có muốn xóa sinh viên hay không !", "Thông báo", JOptionPane.YES_NO_OPTION);
            if (test == JOptionPane.YES_OPTION) {
                LayMaSinhVien(count);
                LayTenDangNhap(count);
                deleteTaiKhoan(tenDangNhap);
                deleteSinhVien(maSV);
            } else if (test == JOptionPane.NO_OPTION) {
                JOptionPane.showMessageDialog(this, "Bạn đã hủy xóa sinh viên thành công");
            }
        }
    }//GEN-LAST:event_deleteSVMouseClicked

