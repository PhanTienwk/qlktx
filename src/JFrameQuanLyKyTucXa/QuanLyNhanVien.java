    private void showTable() {
        listnhanvien = new QuanLyNhanVienDAO().getAllThongTinNV();
        Model.setRowCount(0);
        for (ThongTinNhanVien nv : listnhanvien) {
            ThongTinTaiKhoan tk = new TaiKhoanDAO().getAllThongTinTKEmail(nv.getEmail());
            Model.addRow(new Object[]{
                nv.getMaNV(), nv.getTenNV(), nv.getCCCD(), nv.getGioiTinh(), nv.getNgaySinh(), tk.getPhanQuyen(), nv.getSoDienThoai()
            });
        }
        count = -1;
    }

