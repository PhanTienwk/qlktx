    private void CapNhatSoDienNuoc() {
        float sodiencu = Float.parseFloat(txtsodiencu.getText().trim());
        float sodienmoi = Float.parseFloat(txtsodienmoi.getText().trim());
        float sonuoccu = Float.parseFloat(txtsonuoccu.getText().trim());
        float sonuocmoi = Float.parseFloat(txtsonuocmoi.getText().trim());
        double tienthanhtoan = ((sonuocmoi - sonuoccu) * tiennuoc) + tienmang + ((sodienmoi - sodiencu) * tiendien);

        Connection con = KetNoiSQL.getConnection();
        String sql = "update thongtinhoadon set soNuocMoi=?, soNuocCu=?, soDienMoi=?, soDienCu=?, tienThanhToan=? where maPhong=?";

        try {
            PreparedStatement ps = con.prepareStatement(sql);

            ps.setFloat(1, sonuocmoi);
            ps.setFloat(2, sonuoccu);
            ps.setFloat(3, sodienmoi);
            ps.setFloat(4, sodiencu);
            ps.setString(6, txtmaphong.getText().trim());
            ps.setDouble(5, tienthanhtoan);

            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Cập nhật thành công");
            con.close();
        } catch (Exception e) {
        }

    }
    
