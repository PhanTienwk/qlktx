    void showTable() {
        listhd = new ThongTinHoaDonDAO().getAllThongTinHoaDonSV();
        Model.setRowCount(0);
        for (ThongTinHoaDonSV hdsv : listhd) {
            String ttthanhtoan = null;
            if (hdsv.getThanhToan() == 1) {
                ttthanhtoan = "Đã thanh toán";
            } else {
                ttthanhtoan = "Chưa thanh toán";
            }
            Model.addRow(new Object[]{
                hdsv.getThang(), hdsv.getMaSV(), hdsv.getMaPhong(), hdsv.getTienPhi(), ttthanhtoan
            });
        }
    }

