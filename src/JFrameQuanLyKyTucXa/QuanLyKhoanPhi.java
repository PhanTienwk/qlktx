    private void jLabel5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel5MouseClicked
        // TODO add your handling code here:
        String tiendien = txttiendien.getText().trim();
        String tiennuoc = txttiennuoc.getText().trim();
        String tienmang = txttienmang.getText().trim();

        String kiemtraso = "^[0-9.]+$";

        if (!tiendien.matches(kiemtraso)) {
            JOptionPane.showMessageDialog(this, "Định dạng tiền điện sai. Vui lòng nhập lại!");
        } else if (!tiennuoc.matches(kiemtraso)) {
            JOptionPane.showMessageDialog(this, "Định dạng tiền nước sai. Vui lòng nhập lại!");
        } else if (!tienmang.matches(kiemtraso)) {
            JOptionPane.showMessageDialog(this, "Định dạng tiền nước sai. Vui lòng nhập lại!");
        } else {
            int test = JOptionPane.showConfirmDialog(null, "Bạn chắc có muốn cập nhật tiền dịch vụ hay không !", "Thông báo đăng ký", JOptionPane.YES_NO_OPTION);
            if (test == JOptionPane.YES_OPTION) {
                CapNhatThongTinDichVu();
                CapNhatTienThanhToan();
                updateHoaDonSV();
                showTienDichVu();
                showTable();
            } else if (test == JOptionPane.NO_OPTION) {
                JOptionPane.showMessageDialog(this, "Bạn đã hủy cập nhật thành công");
            }
        }

    }//GEN-LAST:event_jLabel5MouseClicked

